                 </br>&nbsp;&nbsp;health systems more effective & response to the needs of the people . It’s mission is to 
                 </br>&nbsp;&nbsp;serve the rural people. It’s mission is to serve the rural people, especially the poor, downtrodden & weaker sections of the 
				 </br>&nbsp;&nbsp;society, irrespective of their caste, creed, race and religion.It is , due to the continuing efforts of PMT, that turned Loni, a small                 </br>&nbsp;&nbsp;village hamlet into a vibrant township. It is driven by Think Globally & Act Locally ideology. Thus Loni stands testimony to the  
				 </br>&nbsp;&nbsp;provision of quality health care to the rural people and testimony to the provision of excellent training in medical & paramedical fields.</p><p>
