				 </br>&nbsp;&nbsp;disciplinary, superspeciality  hospital with ancillary facilities. To cater to the needs of peripheral population there are extension                 </br>&nbsp;&nbsp;OPD’s and 7  Rural Health Centers. Tele-medicine facility is provided in 40 villages. The Pravara Rural Dental Hospital Provides ,  
